                                    education that favored Christians against Muslims and other colonial injustices against Muslims. Christian
                                    educational, health care, and civil society organizations are more copious, better established and of higher quality
                                    in comparison to Muslim founded organizations. Therefore, CIDA-Uganda was founded as an organization to act
                                    as the national platform for the development and advocacy of Islam and Muslims in Uganda with its doors
                                    remaining open to the Non-Muslims.</p>
                                <p class="JustifyAlign">In 2018, a group of educated, dynamic and like-minded Ugandan Muslims working in the development sector, got
                                    together to establish an organization, for addressing the most urgent and pressing needs of the people of Uganda
                                    especially to the Muslim Community whose Development, Unity and Capacity building are so devastating. True to
                                    this spirit, the organization was named Center for Islamic Development and Advocacy (CIDA)-Uganda.</p>
                                <p class="JustifyAlign">Considering their association with the development sector, the group was cognizant of the plethora of problems
                                    plaguing the Muslim fraternity and the Ugandan Communities at large. Consequently, the organization’s focus is
                                    not kept limited either in terms of geography or sector. CIDA-Uganda is carrying a range of interventions in the
                                    areas of education, health, human rights, charity, youth & women empowerment, emergency response &
                                    preparedness, People with Disabilities Empowerment, governance and legal aid among others.</p>
                                <p class="JustifyAlign">The type of activities undertaken by the organization range from capacity building to service delivery, with focus
                                    remaining on the poor, deprived and marginalized segments of the society whereas in terms of operations, what
                                    characterizes CIDA-Uganda’s functioning are its efforts to (a) enlist involvement of the locals when initiating
                                    activities at the grass root level; and (b) mobilize the population especially women for bringing unity, peace,
                                    tolerance, and development in society.</p>
                                <p class="JustifyAlign">CIDA-Uganda owing to serving Humanity as one of the characteristics of those who follow Prophet Muhammad’s
                                    (PBUH) footsteps, principles and teachings, enjoys a diverse portfolio in representing Islam for Development,
                                    Peace and Unity. In actual sense, Islam represents a soul approaching humans with Love and Compassion. The
                                    sense of responsibility emanating from the seeds of mercy takes human beings to perfection in manners and
                                    happiness in both worlds (here and hereafter). Islam is revival of humanity and therefore, all manners born by
                                    sublime system of Islam are the most humane ones.</p>
                                <p class="JustifyAlign">CIDA-Uganda finds the basis of Islamic ethics in getting closer to Allah the Almighty with Love and Sincerity and
